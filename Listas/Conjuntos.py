#Conjuntos, se determinan con los simbolos de llaves {}

A={1,2,3,4} 
B={2,3,5,6}
C={3,4,6,7}

#Conjuntos equivalentes o iguales
print(A==B)

#Union de conjuntos
print(A|B)

#Interseccion de conjuntos
print(A&C)

#Diferencia de conjuntos, elementos de B que no tiene A
print(B-A)

#Diferencia simetrica
print(A^B)